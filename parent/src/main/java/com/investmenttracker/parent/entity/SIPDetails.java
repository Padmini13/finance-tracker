package com.investmenttracker.parent.entity;

public class SIPDetails {

	private Long amount;
	private Long rateOfReturn;
	private Long invDuration;
	private Long investments;
	private Long SIPReturn;
	
	public Long getAmount() {
		return amount;
	}
	public void setAmount(Long amount) {
		this.amount = amount;
	}
	public Long getRateOfReturn() {
		return rateOfReturn;
	}
	public void setRateOfReturn(Long rateOfReturn) {
		this.rateOfReturn = rateOfReturn;
	}
	public Long getInvDuration() {
		return invDuration;
	}
	public void setInvDuration(Long invDuration) {
		this.invDuration = invDuration;
	}
	public Long getInvestments() {
		return investments;
	}
	public void setInvestments(Long investments) {
		this.investments = investments;
	}
	public Long getSIPReturn() {
		return SIPReturn;
	}
	public void setSIPReturn(Long sIPReturn) {
		SIPReturn = sIPReturn;
	}
	
	
	
}
